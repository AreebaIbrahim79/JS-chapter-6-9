
var a = 10;

// Display the initial value of a
document.write("Result  :" + "<br>");
document.write(" The value of a is: " + a + "<br>");
document.write(".......................................... " + "<br>" + "<br>");

// Incremented value of a
document.write("The value of ++a is: " + (++a) + "<br>");
document.write( " Now the value of a is: " + a + "<br>" + "<br>");

// current value of a
document.write("The value of a++ is: " + (a++) + "<br>");
document.write( " Now the value of a is: " + a + "<br>" + "<br>");

// Decremented value of a
document.write("The value of --a is: " + (--a) + "<br>");
document.write( " Now the value of a is: " + a + "<br>" + "<br>");

//current value of a
document.write("The value of a-- is: " + (a--)+ "<br>");
document.write( " Now the value of a is: " + a + "<br>" + "<br>");